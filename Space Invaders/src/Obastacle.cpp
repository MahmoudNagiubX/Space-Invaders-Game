Obstacle::Obstacle(Vector2 position)
{
    this -> position = position;
    // Iterate over the grid and create a Shield object for each cell that has a 1
    for (unsigned int row = 0; row < grid.size(); ++row)
    {
        for (unsigned int column = 0; column < grid[0].size(); ++column)
        {
            if (grid[row][column] == 1)
            {
                // Calculate the position of the shield based on the cell's position in the grid
                const float CellSize= 3.0;
                float positionX = position.x + column * CellSize;
                float positionY = position.y + row * CellSize;
                shieldsVector.push_back(Shield({positionX, positionY}));
            }
        }
        
    }
}
void Obstacle::Draw()
{    
    for (auto& shield: shieldsVector)
    {
        shield.draw();
    }
}